    private String retornarFecha(Calendar cal) {
        //A este m√©todo le pasamos un objeto calendar (cal) y lo convierte en formato String-Fecha
        String fecha = null;
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        if (cal != null) {
            fecha = sdf.format(cal.getTime());
        }
        return fecha;
    }

    private String retornarHora(Calendar cal) {
        //A este m√©todo le pasamos un objeto calendar (cal) y lo convierte en formato String-hora
        String hora = null;
        SimpleDateFormat sdf = new SimpleDateFormat("HH:mm:ss");
        if (cal != null) {
            hora = sdf.format(cal.getTime());
        }
        return hora;
    }
    
    public Calendar retornarCalendar(String fecha){
        //Se le pasa como argumento un String-Fecha y la convierte en objeto calendar.
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        Calendar cal = Calendar.getInstance();    
        try {
            cal.setTime(sdf.parse(fecha));
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null, "Error: compruebe formato de fecha: " + ex);  
        }
        return cal;
    }

    private void verificar() {
        //M√©todo para verificar que no se duplique el mismo art√≠culo en la tabla,
        //para ello se comprueba el id del art√≠culo.
        var = 0;
        int idAr = Integer.parseInt(txtIdA.getText());
        if(tblVen.getRowCount()>=1){
            for (int i = 0; i < tblVen.getRowCount(); i++) {
                String valueAt = model.getValueAt(i, 0).toString();
                int parseInt = Integer.parseInt(valueAt);
                if(idAr == parseInt){
                    var = 1;
                    idArtRep = i;
                }        
            }        
        } 
    }

    private void sumarRepetido() {
        //Si se repite el ingreso de un art√≠culo espec√≠fico,se puede optar por sumar la cantidad de venta,
        //este m√©todo localiza el id del art√≠culo espec√≠fico y aumenta la cantidad de la
        //venta sumandola.
        int cantidad = Integer.parseInt(txtCanA.getText());
        int total = Integer.parseInt(txtTotA.getText());
        Object cantAnteriorO = model.getValueAt(idArtRep, 3);
        Object totalAnteriorO = model.getValueAt(idArtRep, 4);
        int cantAnterior = Integer.parseInt(cantAnteriorO.toString());
        int totalAnterior = Integer.parseInt(totalAnteriorO.toString());
        int cantNuevo = cantidad + cantAnterior;
        int totalNuevo = total + totalAnterior;
        model.setValueAt(cantNuevo, idArtRep, 3);
        model.setValueAt(totalNuevo, idArtRep, 4);        
    }

    private void calcular() {
        //M√©todo para calcular el total de la venta, obteniendo datos del jtable.
        int totalG = 0;
        for (int i = 0; i < tblVen.getRowCount(); i++) {
            String valueAt = model.getValueAt(i, 4).toString();
            totalG += Integer.parseInt(valueAt);
        }
        txtTotalG.setText(String.valueOf(totalG));
    }

    private void confirmAgregar() {
        //M√©todo que pregunta al usuario si desea agregar m√°s art√≠culo a la lista.
        int opc = JOptionPane.showConfirmDialog(null, "¬øAgregar m√°s Articulos?", "Mensaje del Sistema", JOptionPane.YES_NO_OPTION);
        if(opc == 0){
            txtIdA.requestFocus();
        }
        else{
            btnGuardar.requestFocus();
        }
    }

    private void actualizarStock(int cantidadVendida, int idArticulo) {
        //Desp√∫es que el proceso de guardado se haga de forma correcta, 
        //se actualiza el stock de los art√≠culos ventados.
        st = HibernateUtil.getSessionFactory().openSession();
        st.beginTransaction();
        Articulo actualizar = (Articulo)st.load(Articulo.class, idArticulo);
        int temp = actualizar.getCan() - cantidadVendida;
        actualizar.setCan(temp);
        st.update(actualizar);
        st.getTransaction().commit();
        st.close();
    }

