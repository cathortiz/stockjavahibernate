    private void calcularParcial() {
        //Lo siguiente nos permite calcular el total para cada art√≠culo,
        //esto tomando en cuenta total de venta y precio de venta.
        int canVenta = 0;
        int pVenta = 0;
        int stock = 0;
        int totalParcial = 0 ;
        try {
            canVenta = Integer.parseInt(txtCanA.getText());
            pVenta = Integer.parseInt(txtPComA.getText());
            stock = Integer.parseInt(txtStockA.getText());
            if(stock < canVenta){
                JOptionPane.showMessageDialog(null, "Stock insuficiente.");
            }
            else{
               totalParcial = canVenta * pVenta;
                txtTotA.setText(String.valueOf(totalParcial)); 
                btnAgregar.requestFocus();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Se requieren datos num√©ricos.");
        }
    }

